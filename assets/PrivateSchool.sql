-- Create database
CREATE DATABASE private_school;

USE private_school;

-- Create tables
CREATE TABLE courses (
	course_id int NOT NULL AUTO_INCREMENT,
    title varchar(45),
    stream varchar(45),
    type varchar(45),
    start_date date,
    end_date date,
    PRIMARY KEY(course_id)
);

CREATE TABLE students (
	student_id int NOT NULL AUTO_INCREMENT,
    first_name varchar(45),
    last_name varchar(45),
    dateOfBirth date,
    tuition_fees int,
    PRIMARY KEY(student_id)
);

CREATE TABLE trainers (
	trainer_id int NOT NULL AUTO_INCREMENT,
    first_name varchar(45),
    last_name varchar(45),
    subject varchar(45),
    PRIMARY KEY(trainer_id)
);

CREATE TABLE assignments (
	assignment_id int NOT NULL AUTO_INCREMENT,
    title varchar(45),
    description varchar(300),
    subDateTime date,
    oral_mark int,
    total_mark int,
    PRIMARY KEY(assignment_id)
);

CREATE TABLE studentsPerCourse (
	studentPerCourse_id int NOT NULL AUTO_INCREMENT,
    student_id int,
    course_id int,
    PRIMARY KEY(studentPerCourse_id),
    FOREIGN KEY(student_id) REFERENCES students(student_id),
    FOREIGN KEY(course_id) REFERENCES courses(course_id)
);

CREATE TABLE trainersPerCourse (
	trainerPerCourse_id int NOT NULL AUTO_INCREMENT,
    trainer_id int,
    course_id int,
    PRIMARY KEY(trainerPerCourse_id),
    FOREIGN KEY(trainer_id) REFERENCES trainers(trainer_id),
    FOREIGN KEY(course_id) REFERENCES courses(course_id)
);

CREATE TABLE assignmentsPerCourse (
	assignmentPerCourse_id int NOT NULL AUTO_INCREMENT,
    course_id int,
    assignment_id int,
    PRIMARY KEY(assignmentPerCourse_id),
    FOREIGN KEY(course_id) REFERENCES courses(course_id),
    FOREIGN KEY(assignment_id) REFERENCES assignments(assignment_id)
);

CREATE TABLE assignmentsPerStudentPerCourse (
	assignmentsPerStudentPerCourse_id int NOT NULL AUTO_INCREMENT,
    assignment_id int,
    student_id int,
    course_id int,
    PRIMARY KEY(assignmentsPerStudentPerCourse_id),
    FOREIGN KEY(student_id) REFERENCES students(student_id),
    FOREIGN KEY(assignment_id) REFERENCES assignments(assignment_id),
    FOREIGN KEY(course_id) REFERENCES courses(course_id)
);



