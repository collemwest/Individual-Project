-- A list of all the courses
SELECT * FROM courses;

-- All the students per course
SELECT * FROM students_Per_Course;

CREATE VIEW students_Per_Course AS
SELECT c.title, c.stream, c.type, s.first_name, s.last_name
FROM students AS s, courses AS c, studentsPerCourse AS spc
WHERE s.student_id = spc.student_id AND c.course_id = spc.course_id
ORDER BY c.title;


-- All the trainers per course
SELECT * FROM trainers_Per_Course;

CREATE VIEW trainers_Per_Course AS
SELECT c.title, c.stream, c.type, t.first_name, t.last_name
FROM courses AS c, trainers AS t, trainersPerCourse AS tpc
WHERE c.course_id = tpc.course_id AND t.trainer_id = tpc.trainer_id  
ORDER BY c.title;


-- All the assignments per course
SELECT * FROM assignments_Per_Course;

CREATE VIEW assignments_Per_Course AS
SELECT c.title as courseTitle, c.stream, c.type, a.title, a.description
FROM courses AS c, assignments AS a, assignmentsPerCourse AS apc
WHERE c.course_id = apc.course_id AND a.assignment_id = apc.assignment_id
ORDER BY c.title;


-- All the assignments per course per student
SELECT * FROM assignments_Per_Student_Per_Course;

CREATE VIEW assignments_Per_Student_Per_Course AS
SELECT assignments.title, assignments.subDateTime, courses.stream, courses.type, students.first_name, students.last_name
FROM assignmentsPerStudentPerCourse 
INNER JOIN assignments ON assignmentsPerStudentPerCourse.assignment_id = assignments.assignment_id
INNER JOIN courses ON assignmentsPerStudentPerCourse.course_id = courses.course_id
INNER JOIN students ON assignmentsPerStudentPerCourse.student_id = students.student_id;


-- A list of students that belong to more than one courses
SELECT * FROM studentsToMoreThanOneCourse;

CREATE VIEW studentsToMoreThanOneCourse AS
SELECT  students.student_id, students.first_name, students.last_name, COUNT(*) AS Number_Of_Courses
FROM students
INNER JOIN studentsPerCourse ON studentsPerCourse.student_id = students.student_id
INNER JOIN courses ON studentsPerCourse.course_id = courses.course_id
GROUP BY students.student_id
HAVING COUNT(*) > 1
ORDER BY students.student_id;
